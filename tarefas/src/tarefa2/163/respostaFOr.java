public class respostaFOr {
    public static void main(String[] args) {
        int voltaLagoa, abdominal;

        for (voltaLagoa = 1; voltaLagoa <= 3; voltaLagoa++) {
            System.out.println(voltaLagoa + " voltas na lagoa!");
            for (abdominal = 1; abdominal <= 5; abdominal++) {
                System.out.println(abdominal + " abdominais");
            }
        }
    }
}
